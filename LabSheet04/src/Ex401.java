public class Ex401 {
    public static void main(String[] args) {


        String message = "INT-108 Object-Oriented Programming 2025" ;

        System.out.println("Length message = " + message.length());
        System.out.println("Uppercase = " + message.toUpperCase());
        System.out.println("Lowercase = " + message.toLowerCase());
        System.out.println("First = " + message.charAt(0));
        System.out.println("First = " + message.charAt(message.length()-1));
    }
}
